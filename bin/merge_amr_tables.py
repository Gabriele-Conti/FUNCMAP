def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Merge per-sample AMR tables generated by parse_rgi_bwt.py."
    )

    parser.add_argument(
        "--input-dir",
        required=True,
        help="Directory containing per-sample AMR parsed tables.",
    )

    parser.add_argument(
        "--outdir",
        required=True,
        help="Output directory for merged AMR tables.",
    )

    parser.add_argument(
        "--allow-missing",
        action="store_true",
        help=(
            "Allow missing table types. If not set, the script fails when one "
            "expected table type is absent."
        ),
    )

    return parser.parse_args()


def fail(message: str) -> None:
    print(f"[ERROR] {message}", file=sys.stderr)
    sys.exit(1)


def warn(message: str) -> None:
    print(f"[WARN] {message}", file=sys.stderr)


def find_files(input_dir: Path, suffix: str) -> List[Path]:
    return sorted(input_dir.rglob(f"*{suffix}"))


def read_table(path: Path, keys: List[str]) -> pd.DataFrame:
    if not path.exists():
        fail(f"File not found: {path}")

    if path.stat().st_size == 0:
        fail(f"Empty file: {path}")

    try:
        df = pd.read_csv(path, sep="\t")
    except Exception as exc:
        fail(f"Could not read {path}: {exc}")

    missing_keys = [k for k in keys if k not in df.columns]
    if missing_keys:
        fail(
            f"File {path} is missing key columns: "
            + ", ".join(missing_keys)
        )

    sample_cols = [c for c in df.columns if c not in keys]

    if len(sample_cols) != 1:
        fail(
            f"File {path} should contain exactly one sample/value column "
            f"besides keys {keys}, found: {sample_cols}"
        )

    sample_col = sample_cols[0]

    # Convert sample values to numeric and aggregate possible duplicated features.
    df[sample_col] = pd.to_numeric(df[sample_col], errors="coerce").fillna(0)

    df = (
        df.groupby(keys, dropna=False)[sample_col]
        .sum()
        .reset_index()
    )

    return df


def merge_feature_tables(files: List[Path], keys: List[str]) -> pd.DataFrame:
    tables = [read_table(path, keys) for path in files]

    if not tables:
        return pd.DataFrame(columns=keys)

    merged = reduce(
        lambda left, right: pd.merge(left, right, on=keys, how="outer"),
        tables,
    )

    value_cols = [c for c in merged.columns if c not in keys]
    merged[value_cols] = merged[value_cols].fillna(0)

    # Deterministic ordering.
    merged = merged.sort_values(keys).reset_index(drop=True)

    return merged


def merge_all_feature_tables(
    input_dir: Path,
    outdir: Path,
    allow_missing: bool = False,
) -> None:
    for table_name, spec in TABLE_SPECS.items():
        suffix = spec["suffix"]
        keys = spec["keys"]
        output_name = spec["output"]

        files = find_files(input_dir, suffix)

        if not files:
            message = f"No files found for {table_name} with suffix {suffix}"
            if allow_missing:
                warn(message)
                pd.DataFrame(columns=keys).to_csv(
                    outdir / output_name,
                    sep="\t",
                    index=False,
                )
                continue
            fail(message)

        print(
            f"[INFO] Merging {table_name}: {len(files)} files",
            file=sys.stderr,
        )

        merged = merge_feature_tables(files, keys)
        merged.to_csv(outdir / output_name, sep="\t", index=False)


def read_qc_table(path: Path) -> pd.DataFrame:
    if not path.exists():
        fail(f"QC file not found: {path}")

    if path.stat().st_size == 0:
        fail(f"Empty QC file: {path}")

    try:
        df = pd.read_csv(path, sep="\t")
    except Exception as exc:
        fail(f"Could not read QC file {path}: {exc}")

    if "sample_id" not in df.columns:
        fail(f"QC file {path} is missing required column: sample_id")

    if len(df) != 1:
        warn(f"QC file {path} contains {len(df)} rows; all rows will be kept.")

    return df


def merge_qc_tables(
    input_dir: Path,
    outdir: Path,
    allow_missing: bool = False,
) -> None:
    suffix = ".amr_qc_mapping_stats.tsv"
    files = find_files(input_dir, suffix)

    if not files:
        message = f"No QC files found with suffix {suffix}"
        if allow_missing:
            warn(message)
            pd.DataFrame(columns=["sample_id"]).to_csv(
                outdir / "amr_qc_mapping_stats.tsv",
                sep="\t",
                index=False,
            )
            return
        fail(message)

    print(f"[INFO] Merging AMR QC tables: {len(files)} files", file=sys.stderr)

    qc = pd.concat([read_qc_table(path) for path in files], ignore_index=True)

    # Drop exact duplicate rows if a directory accidentally contains repeated copies.
    qc = qc.drop_duplicates()

    # Sort for deterministic output.
    qc = qc.sort_values("sample_id").reset_index(drop=True)

    qc.to_csv(outdir / "amr_qc_mapping_stats.tsv", sep="\t", index=False)


def main() -> None:
    args = parse_args()

    input_dir = Path(args.input_dir).resolve()
    outdir = Path(args.outdir).resolve()

    if not input_dir.exists():
        fail(f"Input directory not found: {input_dir}")

    if not input_dir.is_dir():
        fail(f"Input path is not a directory: {input_dir}")

    outdir.mkdir(parents=True, exist_ok=True)

    merge_all_feature_tables(
        input_dir=input_dir,
        outdir=outdir,
        allow_missing=args.allow_missing,
    )

    merge_qc_tables(
        input_dir=input_dir,
        outdir=outdir,
        allow_missing=args.allow_missing,
    )

    print(f"[INFO] AMR merge completed. Output directory: {outdir}", file=sys.stderr)


if __name__ == "__main__":
    main()